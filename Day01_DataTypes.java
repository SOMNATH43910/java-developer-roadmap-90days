public class Day01_DataTypes {
    public static void main(String[] args) {
        // Primitive data types
        int age = 25;                  // Integer (whole number)
        float pi = 3.14f;              // Floating-point number
        double price = 9999.99;        // Double-precision floating-point number
        char grade = 'A';              // Character
        boolean isJavaFun = true;      // Boolean value

        // Non-primitive data type
        String name = "Somnath";       // String (non-primitive)

        // Output
        System.out.println("Age: " + age);
        System.out.println("PI: " + pi);
        System.out.println("Price: " + price);
        System.out.println("Grade: " + grade);
        System.out.println("Is Java fun? " + isJavaFun);
        System.out.println("Name: " + name);
    }
}
